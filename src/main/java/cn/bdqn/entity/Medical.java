package cn.bdqn.entity;


public class Medical {

  private long id;
  private String name;
  private long hId;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public long getHId() {
    return hId;
  }

  public void setHId(long hId) {
    this.hId = hId;
  }

}
